def say_hello(name: str, age: int, address: str):
    """
    This is a function for printing out user data.

    Parameters:
        name (str): This is the user name
        age (int): The users age
        address (str): This is the address of the user
    """
    print(f"Hello {name}")
    print(f"You are {age} years old.")
    print(f"You live in {address}")